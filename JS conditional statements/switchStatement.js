var x = 5;

switch (x){
	case 5:
	case 6:
		console.log("x is 6 OR 5");
		break;
	case 7:
		console.log("x is 7");
		break;
	case 8:
		console.log("x is 8");
		break;
	case 9:
		console.log("x is 9");
		break;
	case 10:
		console.log("x is 10");
		break;
	default:
		console.log("NOT 5, 6, 7, 8, 9, 10");
}